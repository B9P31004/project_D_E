$(function(){
    $('div#load').load('/looking_back/load/');
    $(document).on('click',"button[name=button_confirm]",load(300),function(){
        $('div#content').hide();
        $('div#load').show();
        load(300)
        setInterval('load(300)',5400);
    });
});

function load(speed){
    $('#obj1').animate({
        'y':'-30px'
    },speed,function(){
        $("#obj2").animate({
            'y':'-30px'
        },speed,function(){
            $("#obj3").animate({
                'y':'-30px'
            },speed,function(){
                $("#obj4").animate({
                    'y':'-30px'
                },speed,function(){
                    $("#obj5").animate({
                        'y':'-30px'
                    },speed,function(){
                        $("#obj6").animate({
                            'y':'-30px'
                        },speed,function(){
                            $("#obj7").animate({
                                'cy':'230'
                            },speed,function(){
                                $("#obj8").animate({
                                    'cy':'230'
                                },speed,function(){
                                    $("#obj9").animate({
                                        'cy':'230'
                                    },speed,function(){
                                        load_reverse(speed)
                                    })
                                })
                            })
                        })
                    })
                })
            })
        })
    })
}

function load_reverse(speed){
    $('#obj1').animate({
        'y':'0px'
    },speed,function(){
        $("#obj2").animate({
            'y':'0px'
        },speed,function(){
            $("#obj3").animate({
                'y':'0px'
            },speed,function(){
                $("#obj4").animate({
                    'y':'0px'
                },speed,function(){
                    $("#obj5").animate({
                        'y':'0px'
                    },speed,function(){
                        $("#obj6").animate({
                            'y':'0px'
                        },speed,function(){
                            $("#obj7").animate({
                                'cy':'281.228'
                            },speed,function(){
                                $("#obj8").animate({
                                    'cy':'281.228'
                                },speed,function(){
                                    $("#obj9").animate({
                                        'cy':'281.228'
                                    },speed)
                                })
                            })
                        })
                    })
                })
            })
        })
    })
}